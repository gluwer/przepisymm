myLib.str_repeat = function str_repeat(i, m) { for (var o = []; m > 0; o[--m] = i); return(o.join('')); };

myLib.sprintf = function sprintf() {
  var i = 0, a, f = arguments[i++], o = [], m, p, c, x;
  while (f) {
    if (m = /^[^\x25]+/.exec(f)) o.push(m[0]);
    else if (m = /^\x25{2}/.exec(f)) o.push('%');
    else if (m = /^\x25(?:(\d+)\$)?(\+)?(0|'[^$])?(-)?(\d+)?(?:\.(\d+))?([b-fosuxX])/.exec(f)) {
      if (((a = arguments[m[1] || i++]) == null) || (a == undefined)) throw("Too few arguments.");
      if (/[^s]/.test(m[7]) && (typeof(a) != 'number'))
        throw("Expecting number but found " + typeof(a));
      switch (m[7]) {
        case 'b': a = a.toString(2); break;
        case 'c': a = String.fromCharCode(a); break;
        case 'd': a = parseInt(a); break;
        case 'e': a = m[6] ? a.toExponential(m[6]) : a.toExponential(); break;
        case 'f': a = m[6] ? parseFloat(a).toFixed(m[6]) : parseFloat(a); break;
        case 'o': a = a.toString(8); break;
        case 's': a = ((a = String(a)) && m[6] ? a.substring(0, m[6]) : a); break;
        case 'u': a = Math.abs(a); break;
        case 'x': a = a.toString(16); break;
        case 'X': a = a.toString(16).toUpperCase(); break;
      }
      a = (/[def]/.test(m[7]) && m[2] && a > 0 ? '+' + a : a);
      c = m[3] ? m[3] == '0' ? '0' : m[3].charAt(1) : ' ';
      x = m[5] - String(a).length;
      p = m[5] ? myLib.str_repeat(c, x) : '';
      o.push(m[4] ? a + p : p + a);
    }
    else throw ("Huh ?!");
    f = f.substring(m[0].length);
  }
  return o.join('');
};

myLib.escape = function(val) {
  return val.split("&").join("&amp;").split( "<").join("&lt;").split(">").join("&gt;");
};

myLib.openWindowWithPost = function(url,name, options, data) {
  var newWindow = window.open(url, name, options);
  if (!newWindow) return false;
  var html = "";
  html += "<html><head></head><body><form id='formid' method='post' action='" + url + "'>";
  for (var i in data)
    html += "<input type='hidden' name='" + i + "' value='" + myLib.escape(data[i]) + "'/>";
  html += "</form><script type='text/javascript'>document.getElementById(\"formid\").submit()</script></body></html>";
  newWindow.document.write(html);
  return newWindow;
};